package ua.nure.efimov.summarytask4.exception;

/**
 * Exception of Business Layer of application.
 * 
 * @author Alexandr Efimov
 *
 */
public class BusinessException extends ApplicationException {

	/**
	 * SerialVersionUID.
	 */
	private static final long serialVersionUID = -2936719669473331186L;

	/**
	 * Constructs a new Business Exception with {@code null} as its detail
	 * message.
	 * 
	 */
	public BusinessException() {
	}

	/**
	 * Constructs a new Business Exception with the specified detail message.
	 * The cause is not initialized, and may subsequently be initialized by a
	 * call to {@link #initCause}.
	 *
	 * @param message
	 *            the detail message. The detail message is saved for later
	 *            retrieval by the {@link #getMessage()} method.
	 */
	public BusinessException(String message) {
		super(message);
	}

	/**
	 * Constructs a new Business Exception with the specified detail message and
	 * cause.
	 *
	 * @param message
	 *            the detail message
	 * @param cause
	 *            the cause
	 */
	public BusinessException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * Constructs a new Business Exception with the specified cause and a detail
	 * message. It's general exception of application.
	 *
	 * @param cause
	 *            the cause (which is saved for later retrieval by the
	 *            {@link #getCause()} method).
	 */
	public BusinessException(Throwable cause) {
		super(cause);
	}
}
