package ua.nure.efimov.summarytask4.entity;

import ua.nure.efimov.summarytask4.db.dao.Identified;

public class UserRoles implements Identified<Integer> {
	/**
	 * SerialVersion UID.
	 */
	private static final long serialVersionUID = -1861868117747347966L;

	private Integer id;

	/**
	 * Login of user.
	 */
	private String login;

	/**
	 * Role of user.
	 */
	private String roleName;

	@Override
	public Integer getId() {
		return id;
	}

	/**
	 * @return the login
	 */
	public String getLogin() {
		return login;
	}

	/**
	 * @param login
	 *            the login to set
	 */
	public void setLogin(String login) {
		this.login = login;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(Integer id) {
		this.id = id;
	}

	/**
	 * @return the roleName
	 */
	public String getRoleName() {
		return roleName;
	}

	/**
	 * @param roleName
	 *            the roleName to set
	 */
	public void setRoleName(String roleName) {
		this.roleName = roleName;
	}

	@Override
	public String toString() {
		return "UserRoles [id=" + id + ", login=" + login + ", roleName=" + roleName + "]";
	}

}
